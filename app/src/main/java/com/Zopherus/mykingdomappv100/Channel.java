package com.Zopherus.mykingdomappv100;

public class Channel {
    private int mChannelLogo;
    private String mChannelName;

    public Channel(int channelLogo, String channelName) {
        mChannelLogo = channelLogo;
        mChannelName = channelName;
    }

    public int getmChannelLogo() {
        return mChannelLogo;
    }

    public String getmChannelName() {
        return mChannelName;
    }

}
